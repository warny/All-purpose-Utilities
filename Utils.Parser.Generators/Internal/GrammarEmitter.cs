using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using Utils.Parser.Diagnostics.EmbeddedCode;

namespace Utils.Parser.Generators.Internal;

/// <summary>
/// Converts a <see cref="G4Grammar"/> AST into a C# source file that constructs
/// the equivalent <c>Utils.Parser.Model.ParserDefinition</c> programmatically —
/// no runtime .g4 parsing required.
/// </summary>
internal static class GrammarEmitter
{
    private const string GrammarSuffix = "Grammar";

    /// <summary>
    /// Emits a complete C# source file for the given grammar.
    /// </summary>
    /// <param name="grammar">The parsed G4 grammar.</param>
    /// <param name="namespaceName">Namespace for the generated class (may be empty).</param>
    /// <param name="className">Class name for the generated partial class.</param>
    /// <param name="sourceFileName">Original .g4 file name, used in the header comment.</param>
    public static string Emit(
        G4Grammar grammar,
        string namespaceName,
        string className,
        string sourceFileName,
        IParserEmbeddedCodeTransformer? embeddedCodeTransformer = null)
    {
        embeddedCodeTransformer ??= NoOpParserEmbeddedCodeTransformer.Instance;
        var sb = new StringBuilder();
        string stringSyntaxName = GetStringSyntaxName(grammar.Name);
        string[] syntaxKeywords = GetStringSyntaxKeywords(grammar);
        string[] stringSyntaxNonAlphanumericTokens = GetStringSyntaxNonAlphanumericTokens(grammar);
        string[] stringSyntaxNumberRules = GetStringSyntaxRulesByName(grammar, "NUMBER");
        string[] stringSyntaxStringRules = GetStringSyntaxRulesByName(grammar, "STRING");
        var embeddedHooks = CollectEmbeddedCodeHooks(grammar, embeddedCodeTransformer);
        var lifecycleHooks = CollectLifecycleHooks(grammar, embeddedCodeTransformer);
        var parserHeaders = CollectParserHeaders(grammar);
        var parserMembers = CollectParserMembers(grammar);
        var parserFooters = CollectParserFooters(grammar);

        // ── File header ──────────────────────────────────────────────
        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine($"// Source: {sourceFileName}");
        sb.AppendLine("// This file was generated by Utils.Parser.Generators — do not edit manually.");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.AppendLine("using System.Collections.Generic;");
        sb.AppendLine("using Utils.Parser.Model;");
        sb.AppendLine("using Utils.Parser.Resolution;");
        sb.AppendLine("using Utils.Parser.Runtime;");
        sb.AppendLine();

        EmitParserHeaders(sb, parserHeaders, grammar, embeddedCodeTransformer);

        if (!string.IsNullOrEmpty(namespaceName))
        {
            sb.AppendLine($"namespace {namespaceName};");
            sb.AppendLine();
        }

        sb.AppendLine($"/// <summary>Grammar compiled from <c>{sourceFileName}</c>. Do not edit — regenerated on build.</summary>");
        sb.AppendLine($"[global::System.CodeDom.Compiler.GeneratedCode(\"Utils.Parser.Generators\", \"0.1.0\")]");
        sb.AppendLine($"internal static partial class {className}");
        sb.AppendLine("{");
        sb.AppendLine("    /// <summary>Custom <see cref=\"global::System.Diagnostics.CodeAnalysis.StringSyntaxAttribute\"/> name associated with this grammar.</summary>");
        sb.AppendLine($"    public const string StringSyntaxName = \"{Escape(stringSyntaxName)}\";");
        sb.AppendLine();
        sb.AppendLine("    /// <summary>Lexer keywords that can be highlighted for this grammar string syntax.</summary>");
        sb.AppendLine($"    public static IReadOnlyList<string> StringSyntaxKeywords {{ get; }} = new string[] {{ {JoinStringLiterals(syntaxKeywords)} }};");
        sb.AppendLine();
        sb.AppendLine("    /// <summary>Exact lexer literals made only of non-alphanumeric characters, suitable for dedicated operator or punctuation highlighting.</summary>");
        sb.AppendLine($"    public static IReadOnlyList<string> StringSyntaxNonAlphanumericTokens {{ get; }} = new string[] {{ {JoinStringLiterals(stringSyntaxNonAlphanumericTokens)} }};");
        sb.AppendLine();
        sb.AppendLine("    /// <summary>Lexer rule names containing <c>NUMBER</c>, suitable for numeric literal highlighting.</summary>");
        sb.AppendLine($"    public static IReadOnlyList<string> StringSyntaxNumberRules {{ get; }} = new string[] {{ {JoinStringLiterals(stringSyntaxNumberRules)} }};");
        sb.AppendLine();
        sb.AppendLine("    /// <summary>Lexer rule names containing <c>STRING</c>, suitable for string literal highlighting.</summary>");
        sb.AppendLine($"    public static IReadOnlyList<string> StringSyntaxStringRules {{ get; }} = new string[] {{ {JoinStringLiterals(stringSyntaxStringRules)} }};");
        sb.AppendLine();

        // ── BuildDefinition() ────────────────────────────────────────
        sb.AppendLine("    /// <summary>");
        sb.AppendLine($"    /// Constructs the <see cref=\"ParserDefinition\"/> for the <c>{grammar.Name}</c>");
        sb.AppendLine("    /// grammar from pre-compiled rule construction code.");
        sb.AppendLine("    /// No .g4 file parsing occurs at runtime.");
        sb.AppendLine("    /// </summary>");
        sb.AppendLine("    public static ParserDefinition BuildDefinition()");
        sb.AppendLine("    {");
        sb.AppendLine("        int _order = 0;");
        sb.AppendLine();

        // ── Emit lexer rules ─────────────────────────────────────────
        if (grammar.LexerRules.Count > 0)
        {
            sb.AppendLine("        // ── Lexer rules (DEFAULT_MODE) ───────────────────────────");
            foreach (var rule in grammar.LexerRules)
                EmitRule(sb, rule);
            sb.AppendLine();
        }

        // ── Emit extra modes ─────────────────────────────────────────
        foreach (var mode in grammar.ExtraModes)
        {
            sb.AppendLine($"        // ── Lexer rules ({mode.Name}) ─────────────────────────────");
            foreach (var rule in mode.Rules)
                EmitRule(sb, rule);
            sb.AppendLine();
        }

        // ── Emit parser rules ────────────────────────────────────────
        if (grammar.ParserRules.Count > 0)
        {
            sb.AppendLine("        // ── Parser rules ────────────────────────────────────────");
            foreach (var rule in grammar.ParserRules)
                EmitRule(sb, rule);
            sb.AppendLine();
        }

        // ── Build modes ──────────────────────────────────────────────
        sb.AppendLine("        // ── Grammar definition ──────────────────────────────────");
        sb.AppendLine("        return RuleResolver.Resolve(new ParserDefinition(");
        sb.AppendLine($"            Name: \"{Escape(grammar.Name)}\",");

        string grammarType = grammar.Kind switch
        {
            G4GrammarKind.Lexer  => "GrammarType.Lexer",
            G4GrammarKind.Parser => "GrammarType.Parser",
            _                    => "GrammarType.Combined"
        };
        sb.AppendLine($"            Type: {grammarType},");
        EmitGrammarOptions(sb, grammar, indent: 3);
        sb.AppendLine("            Actions: new GrammarAction[0],");
        sb.AppendLine("            Imports: new GrammarImport[0],");

        // modes
        sb.Append("            Modes: new LexerMode[]");
        if (grammar.LexerRules.Count == 0 && grammar.ExtraModes.Count == 0)
        {
            sb.AppendLine(" { },");
        }
        else
        {
            sb.AppendLine();
            sb.AppendLine("            {");
            sb.Append("                new LexerMode(\"DEFAULT_MODE\", new Rule[] {");
            sb.Append(JoinRuleNames(grammar.LexerRules));
            sb.AppendLine(" })");
            foreach (var mode in grammar.ExtraModes)
            {
                sb.Append($",               new LexerMode(\"{Escape(mode.Name)}\", new Rule[] {{");
                sb.Append(JoinRuleNames(mode.Rules));
                sb.AppendLine(" })");
            }
            sb.AppendLine("            },");
        }

        // parserRules
        sb.Append("            ParserRules: new Rule[] {");
        sb.Append(JoinRuleNames(grammar.ParserRules));
        sb.AppendLine(" },");

        // rootRule
        string rootRule = grammar.ParserRules.Count > 0
            ? $"_{Sanitize(grammar.ParserRules[0].Name)}"
            : "null";
        sb.AppendLine($"            RootRule: {rootRule}));");
        sb.AppendLine("    }");
        sb.AppendLine();

        // ── Static Grammar property ──────────────────────────────────
        sb.AppendLine("    /// <summary>Compiled and cached grammar instance.</summary>");
        sb.AppendLine("    public static CompiledGrammar Grammar { get; } = new CompiledGrammar(Build());");
        sb.AppendLine();
        sb.AppendLine("    /// <summary>Tokenizes input using the generated grammar and annotates literals with the custom grammar string syntax.</summary>");
        sb.AppendLine($"    public static IReadOnlyList<Token> Tokenize([global::System.Diagnostics.CodeAnalysis.StringSyntax(StringSyntaxName, typeof({className}))] string input) => Grammar.Tokenize(input);");
        sb.AppendLine();
        sb.AppendLine("    /// <summary>Parses input using the generated grammar and annotates literals with the custom grammar string syntax.</summary>");
        sb.AppendLine($"    public static ParseNode Parse([global::System.Diagnostics.CodeAnalysis.StringSyntax(StringSyntaxName, typeof({className}))] string input) => Grammar.Parse(input);");
        sb.AppendLine();
        EmitEmbeddedCodeFacade(sb, className);

        // ── Build() convenience method ───────────────────────────────
        sb.AppendLine("    /// <summary>Builds and validates the resolved <see cref=\"ParserDefinition\"/>.</summary>");
        sb.AppendLine("    public static ParserDefinition Build() => RuleResolver.Resolve(BuildDefinition());");

        sb.AppendLine("}");
        sb.AppendLine();
        EmitExecutionContext(sb, embeddedHooks, lifecycleHooks, parserMembers, className, sourceFileName);
        EmitParserFooters(sb, parserFooters, grammar, embeddedCodeTransformer);

        return sb.ToString();
    }

    private static void EmitGrammarOptions(StringBuilder sb, G4Grammar grammar, int indent)
    {
        string pad = new string(' ', indent * 4);

        if (grammar.Options.Count == 0)
        {
            sb.AppendLine($"{pad}Options: null,");
            return;
        }

        sb.AppendLine($"{pad}Options: new GrammarOptions(new Dictionary<string, string>");
        sb.AppendLine($"{pad}{{");

        int optionIndex = 0;
        foreach (var option in grammar.Options.OrderBy(item => item.Key, StringComparer.Ordinal))
        {
            string suffix = optionIndex < grammar.Options.Count - 1 ? "," : "";
            sb.AppendLine($"{pad}    [\"{Escape(option.Key)}\"] = \"{Escape(option.Value)}\"{suffix}");
            optionIndex++;
        }

        sb.AppendLine($"{pad}}}),");
    }

    // ── Rule emission ────────────────────────────────────────────────

    private static void EmitRule(StringBuilder sb, G4Rule rule)
    {
        string varName = $"_{Sanitize(rule.Name)}";
        sb.Append($"        var {varName} = new Rule(");
        sb.Append($"\"{Escape(rule.Name)}\", _order++, {(rule.IsFragment ? "true" : "false")}, ");
        EmitContent(sb, rule.Content, 2);
        if (rule.Parameters != null)
        {
            sb.Append($", Parameters: new global::Utils.Parser.Model.RuleParameter[] {{ new global::Utils.Parser.Model.RuleParameter(\"{Escape(rule.Parameters)}\", \"{Escape(rule.Parameters)}\") }}");
        }

        if (rule.Returns != null)
        {
            sb.Append($", Returns: new global::Utils.Parser.Model.RuleReturn[] {{ new global::Utils.Parser.Model.RuleReturn(\"{Escape(rule.Returns)}\", \"{Escape(rule.Returns)}\") }}");
        }

        if (rule.Locals.Count > 0)
        {
            sb.Append(", Locals: new RuleLocal[] { ");
            sb.Append(string.Join(", ", rule.Locals.Select(static local => $"new RuleLocal(\"{Escape(local)}\")")));
            sb.Append(" }");
        }

        sb.AppendLine(");");
    }

    // ── Content emission ─────────────────────────────────────────────

    private static void EmitContent(StringBuilder sb, G4Content content, int indent)
    {
        string pad = new string(' ', indent * 4);

        switch (content)
        {
            case G4Alternation alt:
                EmitAlternation(sb, alt, indent);
                break;

            case G4Sequence seq:
                EmitSequence(sb, seq, indent);
                break;

            case G4Quantifier q:
                sb.Append("new Quantifier(");
                EmitContent(sb, q.Inner, indent);
                sb.Append($", {q.Min}, {(q.Max.HasValue ? q.Max.Value.ToString() : "null")}");
                if (!q.Greedy) sb.Append(", false");
                sb.Append(")");
                break;

            case G4LiteralMatch lit:
                sb.Append($"new LiteralMatch(\"{Escape(lit.Value)}\")");
                break;

            case G4RangeMatch range:
                sb.Append($"new RangeMatch({CharLiteral(range.From)}, {CharLiteral(range.To)})");
                break;

            case G4CharClassMatch cc:
                EmitCharClass(sb, cc);
                break;

            case G4AnyCharMatch:
                sb.Append("new AnyChar()");
                break;

            case G4RuleRef rr:
                if (rr.LabelName != null)
                {
                    string labelArg = $"new RuleLabel(\"{Escape(rr.LabelName)}\", \"{Escape(rr.RuleName)}\", {(rr.LabelIsAdditive ? "true" : "false")})";
                    if (rr.RawArguments != null)
                        sb.Append($"new RuleRef(\"{Escape(rr.RuleName)}\", Label: {labelArg}, RawArguments: \"{Escape(rr.RawArguments)}\")");
                    else
                        sb.Append($"new RuleRef(\"{Escape(rr.RuleName)}\", Label: {labelArg})");
                }
                else if (rr.RawArguments != null)
                    sb.Append($"new RuleRef(\"{Escape(rr.RuleName)}\", RawArguments: \"{Escape(rr.RawArguments)}\")");
                else
                    sb.Append($"new RuleRef(\"{Escape(rr.RuleName)}\")");
                break;

            case G4Negation neg:
                sb.Append("new Negation(");
                EmitContent(sb, neg.Inner, indent);
                sb.Append(")");
                break;

            case G4LexerCommand cmd:
                EmitLexerCommand(sb, cmd);
                break;

            case G4EmbeddedAction act:
                if (act.IsPredicate)
                    sb.Append($"new ValidatingPredicate(\"{Escape(act.Code)}\")");
                else
                    sb.Append($"new EmbeddedAction(\"{Escape(act.Code)}\", ActionContext.Alternative, ActionPosition.Inline, new LabelRef[0])");
                break;
        }
    }

    private static void EmitAlternation(StringBuilder sb, G4Alternation alt, int indent)
    {
        if (alt.Alternatives.Count == 1 && alt.Alternatives[0].Items.Count == 1)
        {
            // Single-alternative, single-item: emit content directly wrapped in a minimal Alternation
            EmitSingleAltWrap(sb, alt.Alternatives[0], indent);
            return;
        }

        sb.AppendLine("new Alternation(new Alternative[]");
        sb.AppendLine(new string(' ', indent * 4) + "{");

        for (int i = 0; i < alt.Alternatives.Count; i++)
        {
            var alternative = alt.Alternatives[i];
            sb.Append(new string(' ', (indent + 1) * 4));
            EmitAlternative(sb, alternative, indent + 1);
            if (i < alt.Alternatives.Count - 1) sb.AppendLine(",");
            else sb.AppendLine();
        }

        sb.Append(new string(' ', indent * 4) + "})");
    }

    private static void EmitSingleAltWrap(StringBuilder sb, G4Alternative alt, int indent)
    {
        sb.AppendLine("new Alternation(new Alternative[]");
        sb.AppendLine(new string(' ', indent * 4) + "{");
        sb.Append(new string(' ', (indent + 1) * 4));
        EmitAlternative(sb, alt, indent + 1);
        sb.AppendLine();
        sb.Append(new string(' ', indent * 4) + "})");
    }

    private static void EmitAlternative(StringBuilder sb, G4Alternative alt, int indent)
    {
        string pad = new string(' ', indent * 4);
        sb.Append($"new Alternative({alt.Priority}, Associativity.Left, ");

        var items = alt.Items;

        if (items.Count == 0)
        {
            sb.Append("new Sequence(new RuleContent[0])");
        }
        else if (items.Count == 1 && !(items[0] is G4Sequence))
        {
            EmitContent(sb, items[0], indent + 1);
        }
        else
        {
            EmitSequenceFromItems(sb, items, indent + 1);
        }

        sb.Append(")");
    }

    private static void EmitSequence(StringBuilder sb, G4Sequence seq, int indent)
    {
        EmitSequenceFromItems(sb, seq.Items, indent);
    }

    private static void EmitSequenceFromItems(StringBuilder sb, List<G4Content> items, int indent)
    {
        if (items.Count == 1)
        {
            EmitContent(sb, items[0], indent);
            return;
        }

        string pad  = new string(' ', indent * 4);
        string pad1 = new string(' ', (indent + 1) * 4);

        sb.AppendLine("new Sequence(new RuleContent[]");
        sb.AppendLine(pad + "{");

        for (int i = 0; i < items.Count; i++)
        {
            sb.Append(pad1);
            EmitContent(sb, items[i], indent + 1);
            if (i < items.Count - 1) sb.AppendLine(",");
            else sb.AppendLine();
        }

        sb.Append(pad + "})");
    }

    private static void EmitCharClass(StringBuilder sb, G4CharClassMatch cc)
    {
        sb.Append($"new CharSetMatch(new HashSet<char> {{");
        bool first = true;
        foreach (var (lo, hi) in cc.Entries)
        {
            if (hi.HasValue)
            {
                // Range: expand all chars
                for (char c = lo; c <= hi.Value; c++)
                {
                    if (!first) sb.Append(", ");
                    sb.Append(CharLiteral(c));
                    first = false;
                }
            }
            else
            {
                if (!first) sb.Append(", ");
                sb.Append(CharLiteral(lo));
                first = false;
            }
        }
        sb.Append($"}}, {(cc.Negated ? "true" : "false")})");
    }

    private static void EmitLexerCommand(StringBuilder sb, G4LexerCommand cmd)
    {
        string cmdType = cmd.Name.ToLowerInvariant() switch
        {
            "skip"     => "LexerCommandType.Skip",
            "more"     => "LexerCommandType.More",
            "channel"  => "LexerCommandType.Channel",
            "type"     => "LexerCommandType.Type",
            "pushmode" => "LexerCommandType.PushMode",
            "popmode"  => "LexerCommandType.PopMode",
            "mode"     => "LexerCommandType.Mode",
            _          => $"LexerCommandType.Skip /* unknown: {cmd.Name} */"
        };

        string arg = cmd.Arg != null ? $"\"{Escape(cmd.Arg)}\"" : "null";
        sb.Append($"new LexerCommand({cmdType}, {arg})");
    }


    /// <summary>
    /// Emits generated facade helpers that opt in to generated embedded-code execution through a per-parse context.
    /// </summary>
    /// <param name="sb">Source builder receiving generated C#.</param>
    /// <param name="className">Generated grammar class name.</param>
    private static void EmitEmbeddedCodeFacade(StringBuilder sb, string className)
    {
        string contextClassName = GetExecutionContextClassName(className);

        sb.AppendLine("    /// <summary>Creates a runtime policy bound to the supplied execution context. Reusing the policy reuses that context state.</summary>");
        sb.AppendLine("    /// <param name=\"executionContext\">Execution context instance that owns generated hooks and injected parser members.</param>");
        sb.AppendLine("    /// <param name=\"basePolicy\">Optional policy whose non-embedded-code components are preserved.</param>");
        sb.AppendLine("    /// <returns>A runtime policy bound to <paramref name=\"executionContext\"/>.</returns>");
        sb.AppendLine($"    public static ParserRuntimeFeaturePolicy CreateRuntimePolicy({contextClassName} executionContext, ParserRuntimeFeaturePolicy? basePolicy = null)");
        sb.AppendLine("    {");
        sb.AppendLine("        global::System.ArgumentNullException.ThrowIfNull(executionContext);");
        sb.AppendLine("        return executionContext.CreateRuntimePolicy(basePolicy);");
        sb.AppendLine("    }");
        sb.AppendLine();
        sb.AppendLine("    /// <summary>Parses input using generated C# hooks and injected parser members. Creates a fresh execution context for this parse.</summary>");
        sb.AppendLine($"    public static ParseNode ParseWithEmbeddedCode([global::System.Diagnostics.CodeAnalysis.StringSyntax(StringSyntaxName, typeof({className}))] string input)");
        sb.AppendLine("    {");
        sb.AppendLine($"        return ParseWithEmbeddedCode(input, new {contextClassName}());");
        sb.AppendLine("    }");
        sb.AppendLine();
        sb.AppendLine("    /// <summary>Parses input using generated C# hooks bound to the supplied execution context. Reusing the same context intentionally preserves its member state across parses.</summary>");
        sb.AppendLine("    /// <param name=\"input\">Input text to parse.</param>");
        sb.AppendLine("    /// <param name=\"executionContext\">Execution context instance that owns generated hooks and injected parser members.</param>");
        sb.AppendLine("    /// <returns>The parse tree produced by the generated grammar.</returns>");
        sb.AppendLine($"    public static ParseNode ParseWithEmbeddedCode([global::System.Diagnostics.CodeAnalysis.StringSyntax(StringSyntaxName, typeof({className}))] string input, {contextClassName} executionContext)");
        sb.AppendLine("    {");
        sb.AppendLine("        global::System.ArgumentNullException.ThrowIfNull(executionContext);");
        sb.AppendLine("        var grammar = new CompiledGrammar(Build(), executionContext.CreateRuntimePolicy());");
        sb.AppendLine("        return grammar.Parse(input);");
        sb.AppendLine("    }");
        sb.AppendLine();
        sb.AppendLine("    /// <summary>Parses input using generated C# hooks plus an explicit base runtime policy. Existing embedded-code components are replaced while other base-policy components, including rule-call execution, are preserved.</summary>");
        sb.AppendLine("    /// <param name=\"input\">Input text to parse.</param>");
        sb.AppendLine("    /// <param name=\"executionContext\">Execution context instance that owns generated hooks and injected parser members.</param>");
        sb.AppendLine("    /// <param name=\"basePolicy\">Base runtime policy whose non-embedded-code components are preserved.</param>");
        sb.AppendLine("    /// <returns>The parse tree produced by the generated grammar.</returns>");
        sb.AppendLine($"    public static ParseNode ParseWithEmbeddedCode([global::System.Diagnostics.CodeAnalysis.StringSyntax(StringSyntaxName, typeof({className}))] string input, {contextClassName} executionContext, ParserRuntimeFeaturePolicy basePolicy)");
        sb.AppendLine("    {");
        sb.AppendLine("        global::System.ArgumentNullException.ThrowIfNull(executionContext);");
        sb.AppendLine("        global::System.ArgumentNullException.ThrowIfNull(basePolicy);");
        sb.AppendLine("        var grammar = new CompiledGrammar(Build(), executionContext.CreateRuntimePolicy(basePolicy));");
        sb.AppendLine("        return grammar.Parse(input);");
        sb.AppendLine("    }");
        sb.AppendLine();
    }

    /// <summary>
    /// Emits the per-parse execution context that owns generated embedded-code hooks and injected parser members.
    /// </summary>
    /// <param name="sb">Source builder receiving generated C#.</param>
    /// <param name="hooks">Collected parser embedded-code hooks.</param>
    /// <param name="lifecycleHooks">Collected rule lifecycle (@init / @after) hooks.</param>
    /// <param name="parserMembers">Parser members blocks to inject verbatim.</param>
    /// <param name="className">Generated grammar class name.</param>
    /// <param name="sourceFileName">Original .g4 file name, used in generated XML documentation.</param>
    private static void EmitExecutionContext(
        StringBuilder sb,
        IReadOnlyList<EmbeddedCodeHook> hooks,
        IReadOnlyList<LifecycleHook> lifecycleHooks,
        IReadOnlyList<G4GrammarAction> parserMembers,
        string className,
        string sourceFileName)
    {
        var predicates = hooks.Where(static hook => hook.IsPredicate).ToList();
        var actions = hooks.Where(static hook => !hook.IsPredicate).ToList();
        string contextClassName = GetExecutionContextClassName(className);

        sb.AppendLine($"/// <summary>Per-parse execution context for generated embedded C# code compiled from <c>{EscapeXml(sourceFileName)}</c>.</summary>");
        sb.AppendLine("[global::System.CodeDom.Compiler.GeneratedCode(\"Utils.Parser.Generators\", \"0.1.0\")]");
        sb.AppendLine($"internal sealed partial class {contextClassName}");
        sb.AppendLine("{");
        EmitParserMembers(sb, parserMembers);
        sb.AppendLine("    /// <summary>Creates a copied execution context that can be used by future speculative parser execution paths.</summary>");
        sb.AppendLine("    /// <remarks>The copy follows <c>ParserExecutionContextCopier&lt;TContext&gt;</c> semantics.</remarks>");
        sb.AppendLine($"    /// <returns>A copied <see cref=\"{contextClassName}\"/> instance.</returns>");
        sb.AppendLine($"    internal {contextClassName} Fork()");
        sb.AppendLine("    {");
        sb.AppendLine($"        return global::Utils.Parser.Runtime.ParserExecutionContextCopier<{contextClassName}>.Copy(");
        sb.AppendLine("            this,");
        sb.AppendLine($"            static () => new {contextClassName}());");
        sb.AppendLine("    }");
        sb.AppendLine();
        sb.AppendLine("    /// <summary>Copies execution-context state from the supplied source into this instance.</summary>");
        sb.AppendLine("    /// <remarks>This is intended for future commit/restore scenarios and is not invoked automatically by <c>ParserEngine</c>.</remarks>");
        sb.AppendLine($"    /// <param name=\"source\">Source <see cref=\"{contextClassName}\"/> whose state is copied into this instance.</param>");
        sb.AppendLine("    internal void CopyFrom(" + contextClassName + " source)");
        sb.AppendLine("    {");
        sb.AppendLine("        global::System.ArgumentNullException.ThrowIfNull(source);");
        sb.AppendLine();
        sb.AppendLine($"        global::Utils.Parser.Runtime.ParserExecutionContextCopier<{contextClassName}>.CopyTo(");
        sb.AppendLine("            source,");
        sb.AppendLine("            this);");
        sb.AppendLine("    }");
        sb.AppendLine();
        sb.AppendLine("    /// <summary>Gets the semantic state key for this execution context instance.</summary>");
        sb.AppendLine("    /// <returns>A deterministic parser execution-state key for the current context state.</returns>");
        sb.AppendLine("    internal global::Utils.Parser.Runtime.ParserExecutionStateKey GetExecutionStateKey()");
        sb.AppendLine("    {");
        sb.AppendLine($"        return global::Utils.Parser.Runtime.ParserExecutionContextHasher<{contextClassName}>.GetKey(this);");
        sb.AppendLine("    }");
        sb.AppendLine();
        sb.AppendLine("    /// <summary>Last completed child call result captured from a successful child-rule exit; included in managed execution-state snapshots for rollback safety.</summary>");
        sb.AppendLine($"    internal global::Utils.Parser.Runtime.ParserRuleCallResult? _lastChildCallResult;");
        sb.AppendLine();
        sb.AppendLine("    /// <summary>Pending child-parameter seeds for the next named child rule invocation; included in managed execution-state snapshots so seeds do not leak across failed parser alternatives.</summary>");
        sb.AppendLine($"    internal global::Utils.Parser.Runtime.ParserRuleParameterSeedStore? _pendingChildSeeds;");
        sb.AppendLine();
